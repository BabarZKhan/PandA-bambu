#include "math_private.h"

/*
 * isnan(x) returns 1 is x is nan, else 0;
 * no branching!
 */

int __isnan(double x)
{
   int hx, lx;
   hx = (GET_HI(x) & 0x7fffffff);
   lx = GET_LO(x);
   hx |= (unsigned)(lx | (-lx)) >> 31;
   hx = 0x7ff00000 - hx;
   return ((unsigned)(hx)) >> 31;
}

int isnan(double x)
{
   return __isnan(x);
}
